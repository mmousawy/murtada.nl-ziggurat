        </figure>
        <p>We did notice there was still a lot of text visible on every page. It looked like a massive wall of text. We were afraid that it might be a moment for the reader to give up on actually reading the text. So we decided to visually organise the visible text in manageable chunks with a clear title for the reader to open. This way, you can quickly skim through the topics of the text without having to scroll at all.</p>
        <p>To finish the website, it needed some more graphics. I opted for hand-drawn digital illustrations. They're easy to create and can still portray a strong message on any page you use them. The style I chose is something that's popular right now: "flat illustration". It's a neutral style that can look clinical and grown-up without being too detailed or dull.</p>
        <figure class="picture-wrapper">
